#Break statement
for i in range(100):
    if(i==35):
        break  #Exit the loop right now
    print(i)

#continue statement -- skip the iteration

for i in range(100):
    if(i==35):
        continue  #skip the current iteration
    print(i)
    
#pass statement

for i in range(494):
    pass #this skip the whole loop 

